package com.gmail.fursovych20.db.dao.exception;

public class DAOException extends Exception {

	private static final long serialVersionUID = 3422480667093173240L;

	public DAOException() {
		super();
	}

	public DAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public DAOException(String message) {
		super(message);
	}

	public DAOException(Throwable cause) {
		super(cause);
	}
	
	

}
